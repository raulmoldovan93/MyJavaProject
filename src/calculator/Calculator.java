package calculator;

public class Calculator {

    public int adunare(int primulNumar, int alDoileaNumar) {
        int resultat = primulNumar + alDoileaNumar;
        return resultat;
    }

    public int scadere(int primulNumar, int alDolieaNumar) {
        int resultat = primulNumar - alDolieaNumar;
        return resultat;
    }

    public int inmultire(int primulNumar, int alDoileaNumar) {
        int resultat = primulNumar * alDoileaNumar;
        return resultat;
    }

    public int impartire(int primulNumar, int alDoileaNumar) {
        int resultat = primulNumar / alDoileaNumar;
        return resultat;
    }

    public int product(int x, int y) {
        int resultat = x * y;
        return resultat;
    }

    public int averege(int x, int y, int z) {
        int resultat = (x + y + z) / 3;
        return resultat;
    }

    public int addition(int firstNumber, int secondNumber) {
        int resultat = firstNumber + secondNumber;
        return resultat;
    }

    public int multiply(int firstNumber, int secondNumber) {
        int resultat = firstNumber * secondNumber;
        return resultat;
    }

    public int substract(int firstNumber, int secondNumber) {
        int resultat = firstNumber - secondNumber;
        return resultat;
    }

    public int divide(int firstNumber, int secondNumber) {
        int resultat = firstNumber / secondNumber;
        return resultat;
    }

    public int remainder(int fisrtNumber, int secondNumber) {
        int resultat = fisrtNumber % secondNumber;
        return resultat;
    }

}





























